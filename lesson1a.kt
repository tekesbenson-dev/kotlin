fun main() {
    // A variable is a container that stores data or values.
    // In Kotlin we have two types of initializers i.e. var and val
    // val => immutable (unchangeable) but var => changeable

    val name = "Benson Seyanga"   // String => series of characters
    val number = 5             // Integer => whole number
    val number2 = 40.6         // Double => decimal number
    var letter = 'k'           // Char => single character
    val isRegistered = true    // Boolean

    println(name)
    println(number)
    println(number2)
    println(letter)
    println(isRegistered)
}